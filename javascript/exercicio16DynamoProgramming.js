function maxWarehouseValue(C, N, prices, volume) {
    const dp = Array(C + 1).fill(0)

    for (let j = 0; j < N; j++) {
        for (let i = C; i >= volume[j]; i--) {
            if (i - volume[j] >= 0) {
                dp[i] = Math.max(dp[i], dp[i - volume[j]] + prices[j])
            }
        }
    }

    return dp[C]
}

